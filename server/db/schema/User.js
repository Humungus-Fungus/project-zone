const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    require: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  created_at: {
    type: String,
    required: true,
  },
});

const UserModel = mongoose.model('user', userSchema);
module.exports = UserModel;
